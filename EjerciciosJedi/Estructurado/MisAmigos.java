public class MisAmigos {
    public static void main (String[] args){
        String[] amigos = {"Juan", "Pedro", "Lucía", "María", "Ana"};

        System.out.println("Los nombres de los amigos son:");
        // Iteramos sobre el array para mostrar los nombres de los amigos
        for (int i = 0; i < amigos.length; i++) {
            System.out.println(amigos[i]);
        }
    }
    
}
